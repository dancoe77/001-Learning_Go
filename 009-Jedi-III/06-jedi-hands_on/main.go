package main

import "fmt"

func main() {
	for i := 70; i <= 75; i++ {
		if i == 70 {
			fmt.Println("our value was 70")
		} else if i == 71 {
			fmt.Println("our value was 71")
		} else if i == 72 {
			fmt.Println("our value was 72")
		} else if i == 73 {
			fmt.Println("our value was 73")
		} else if i == 74 {
			fmt.Println("our value was 74")
		} else {
			fmt.Println("our value was 75")
		}
	}
}
